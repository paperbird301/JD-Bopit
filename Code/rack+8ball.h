void setup() {
  if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    for (;;);
  }
  Wire.begin();
  Wire.setClock(400000);
  display.clearDisplay();
  display.display();  // Send once — image stays on screen
}

void loop() {
  display.clearDisplay();
  display.drawBitmap(32, 0, eight_ball, FRAME_WIDTH, FRAME_HEIGHT, 1);
  display.display();  // Send once — image stays on screen
  delay(5000);

  display.clearDisplay();
  display.drawBitmap(32, 0, rack, FRAME_WIDTH, FRAME_HEIGHT, 1);
  display.display();  // Send once — image stays on screen
  delay(5000);
}